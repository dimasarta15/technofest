    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $ev = Event::whereSemesterId($id)->orderBy('id', 'desc')
            ->whereLang(clearDot(getLang()))
            ->paginate(12);
        
        $data['events'] = $ev;
        $data['semester'] = Semester::findOrFail($id);
        
        return view('frontsite.event.show', $data);
    }

